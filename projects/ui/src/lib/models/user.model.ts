/** @format */

/**
 * Модель пользователя системы
 * Содержит всю необходимую информацию о пользователе для отображения в UI компонентах
 */
export interface User {
  /** Уникальный идентификатор пользователя */
  id: number;

  /** Email адрес пользователя */
  email: string;

  /** Имя пользователя */
  firstName: string;

  /** Фамилия пользователя */
  lastName: string;

  /** URL изображения аватара пользователя */
  avatar: string;

  /** Статус онлайн (true - пользователь в сети, false - оффлайн) */
  isOnline: boolean;

  /** Статус активности аккаунта (true - активен, false - заблокирован/неактивен) */
  isActive: boolean;

  /** Текущий этап процесса онбординга (null если онбординг завершен) */
  onboardingStage: number | null;

  /** Специальность/профессия пользователя */
  speciality: string;

  /** Тип пользователя (числовой код, определяющий роль в системе) */
  userType: number;

  /** Дата и время создания аккаунта в ISO формате */
  timeCreated: string;

  /** Дата и время последнего обновления профиля в ISO формате */
  timeUpdated: string;

  /** Дата верификации аккаунта в ISO формате */
  verificationDate: string;
}
