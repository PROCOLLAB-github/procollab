/**
 * Перечисление HTTP кодов ошибок
 *
 * Назначение:
 * - Определяет стандартные HTTP коды ошибок, используемые в приложении
 * - Обеспечивает типобезопасность при работе с кодами ошибок
 * - Используется для маршрутизации на соответствующие страницы ошибок
 *
 * Коды:
 * - NOT_FOUND: 404 - страница или ресурс не найден
 * - SERVER_ERROR: 500 - внутренняя ошибка сервера
 *
 * Использование:
 * - В ErrorService для навигации на страницы ошибок
 * - В компонентах для проверки типа ошибки
 * - В HTTP interceptors для обработки ошибок
 */

export enum ErrorCode {
  NOT_FOUND = "404", // Ресурс не найден
  SERVER_ERROR = "500", // Внутренняя ошибка сервера
}
