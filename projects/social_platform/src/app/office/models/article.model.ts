/**
 * Модель новости/статьи
 * Представляет новостную статью или объявление в системе
 *
 * @format
 */

export class New {
  /** Уникальный идентификатор новости */
  id!: number;
  /** URL обложки новости */
  coverUrl!: string;
  /** Заголовок новости */
  title!: string;
  /** Полный текст новости (опционально) */
  text?: string;
  /** Краткое описание новости для превью */
  shortText!: string;
}
