/**
 * Интерфейс для подтверждения навыка
 * Содержит информацию о пользователе, который подтвердил навык
 *
 * @format
 */

export interface Approve {
  /** Информация о пользователе, подтвердившем навык */
  confirmedBy: {
    /** Идентификатор подтверждающего пользователя */
    id: number;
    /** Имя подтверждающего пользователя */
    firstName: string;
    /** Фамилия подтверждающего пользователя */
    lastName: string;
    /** URL аватара подтверждающего пользователя */
    avatar: string;
    /** Специальность подтверждающего пользователя */
    speciality: string;
    /** Информация о специальности версии 2 */
    v2Speciality: {
      /** Идентификатор специальности */
      id: number;
      /** Название специальности */
      name: string;
    };
  };
}

/**
 * Интерфейс навыка
 * Представляет конкретный навык пользователя с категорией и подтверждениями
 */
export interface Skill {
  /** Уникальный идентификатор навыка */
  id: number;
  /** Название навыка */
  name: string;
  /** Категория, к которой относится навык */
  category: {
    /** Идентификатор категории */
    id: number;
    /** Название категории */
    name: string;
  };
  /** Массив подтверждений данного навыка от других пользователей */
  approves: Approve[];
}
