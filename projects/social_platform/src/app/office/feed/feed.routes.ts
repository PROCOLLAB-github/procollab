/** @format */

import { Routes } from "@angular/router";
import { FeedComponent } from "@office/feed/feed.component";
import { FeedResolver } from "@office/feed/feed.resolver";

/**
 * МАРШРУТЫ ДЛЯ МОДУЛЯ ЛЕНТЫ НОВОСТЕЙ
 *
 * Определяет конфигурацию маршрутизации для функциональности ленты новостей.
 * Настраивает связь между URL путями и компонентами, а также предварительную загрузку данных.
 *
 * КОНФИГУРАЦИЯ МАРШРУТА:
 * - Путь: "" (корневой путь модуля)
 * - Компонент: FeedComponent (основной компонент ленты)
 * - Резолвер: FeedResolver (предварительная загрузка данных)
 *
 * ПРИНЦИП РАБОТЫ:
 * 1. При навигации на маршрут ленты активируется FeedResolver
 * 2. Резолвер загружает начальные данные ленты с сервера
 * 3. Данные передаются в FeedComponent через свойство 'data'
 * 4. Компонент отображается с предзагруженными данными
 *
 * ПРЕИМУЩЕСТВА ИСПОЛЬЗОВАНИЯ РЕЗОЛВЕРА:
 * - Данные загружаются до отображения компонента
 * - Пользователь не видит пустую страницу во время загрузки
 * - Улучшенный пользовательский опыт
 * - Централизованная логика загрузки данных
 */
export const FEED_ROUTES: Routes = [
  {
    path: "", // Корневой путь модуля ленты
    component: FeedComponent, // Основной компонент для отображения
    resolve: {
      data: FeedResolver, // Предварительная загрузка данных через резолвер
    },
  },
];
