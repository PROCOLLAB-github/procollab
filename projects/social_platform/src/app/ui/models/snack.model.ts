/** @format */

/**
 * Модель для уведомлений snackbar.
 * Определяет структуру данных для всплывающих уведомлений в приложении.
 *
 * Свойства:
 * - id: уникальный идентификатор уведомления
 * - text: текст уведомления для отображения
 * - timeout: время отображения в миллисекундах
 * - type: тип уведомления (ошибка, успех, информация)
 */
export class Snack {
  /** Уникальный идентификатор уведомления */
  id!: string;

  /** Текст уведомления */
  text!: string;

  /** Время отображения в миллисекундах */
  timeout!: number;

  /** Тип уведомления */
  type!: "error" | "success" | "info";
}
