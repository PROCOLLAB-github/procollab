/** @format */

/**
 * Интерфейс для представления спикера вебинара
 *
 * Содержит основную информацию о ведущем вебинара,
 * включая персональные данные и профессиональную информацию
 */
interface Speaker {
  /** Полное имя спикера (имя и фамилия) */
  fullName: string;
  /** URL фотографии спикера для отображения в интерфейсе */
  photo: string;
  /** Должность или профессиональная позиция спикера */
  position: string;
}

/**
 * Модель вебинара
 *
 * Представляет собой онлайн-семинар или презентацию с полной информацией
 * о событии, включая временные рамки, статус регистрации и данные спикера
 */
export class Webinar {
  /** Уникальный идентификатор вебинара в системе */
  id!: number;

  /** Название/заголовок вебинара */
  title!: string;

  /** Подробное описание содержания и целей вебинара */
  description!: string;

  /** Дата и время начала вебинара */
  datetimeStart!: Date;

  /** Продолжительность вебинара в минутах */
  duration!: number;

  /**
   * Статус регистрации текущего пользователя на вебинар
   * null - статус неизвестен
   * true - пользователь зарегистрирован
   * false - пользователь не зарегистрирован
   */
  isRegistrated!: boolean | null;

  /**
   * Ссылка на запись вебинара (для завершенных вебинаров)
   * null - запись недоступна
   * true/false - статус доступности записи
   */
  recordingLink!: boolean | null;

  /** Информация о спикере, ведущем вебинар */
  speaker!: Speaker;
}
