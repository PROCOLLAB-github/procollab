/**
 * Интерфейс для общего рейтинга пользователей
 *
 * Представляет информацию о пользователе в системе рейтингов,
 * включая персональные данные, профессиональную информацию
 * и достижения в обучении
 */
export interface GeneralRating {
  /** Имя пользователя для отображения в рейтинге */
  userName: string;

  /** Возраст пользователя */
  age: number;

  /** Профессиональная специализация или область деятельности */
  specialization: string;

  /** Географическое местоположение пользователя */
  geoPosition: string;

  /** Количество набранных баллов/очков в системе рейтинга */
  scoreCount: number;

  /**
   * Ссылка на файл аватара пользователя
   * null - аватар не установлен, используется заглушка
   */
  file: string | null;
}
