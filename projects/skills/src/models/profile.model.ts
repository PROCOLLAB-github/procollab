/** @format */

/**
 * Интерфейс для основных данных пользователя
 *
 * Содержит персональную информацию, статистику и статус
 * пользователя в системе обучения
 */
export interface UserData {
  /** Уникальный идентификатор пользователя */
  id: number;

  /** Имя пользователя */
  firstName: string;

  /** Фамилия пользователя */
  lastName: string;

  /** Ссылка на основной файл профиля (резюме, портфолио) */
  fileLink: string;

  /** URL аватара пользователя */
  avatar: string;

  /** Возраст пользователя */
  age: number;

  /** Профессиональная специализация */
  specialization: string;

  /** Географическое местоположение */
  geoPosition: string;

  /** Дата верификации профиля в формате строки */
  verificationDate: string;

  /** Общее количество накопленных баллов */
  points: number;

  /** Статус ментора - может ли пользователь обучать других */
  isMentor: boolean;
}

/**
 * Интерфейс для навыка пользователя
 *
 * Представляет конкретный навык с информацией о прогрессе
 * и текущем уровне владения
 */
export interface Skill {
  /** Уникальный идентификатор навыка */
  skillId: number;

  /** Название навыка */
  skillName: string;

  /** Текущий уровень владения навыком (1-10) */
  skillLevel: number;

  /** Прогресс изучения навыка в процентах (0-100) */
  skillProgress: number;

  /** Ссылка на дополнительные материалы по навыку */
  fileLink: string;
}

/**
 * Интерфейс для месячной активности
 *
 * Отслеживает активность пользователя по месяцам
 * для построения календаря достижений
 */
export interface Month {
  /** Название месяца */
  month: string;

  /** Был ли месяц успешно завершен (выполнены цели) */
  successfullyDone: boolean;

  /** Год (опционально, для исторических данных) */
  year?: number;
}

/**
 * Основной интерфейс профиля пользователя
 *
 * Объединяет всю информацию о пользователе:
 * персональные данные, навыки и историю активности
 */
export interface Profile {
  /** Основные данные пользователя */
  userData: UserData;

  /** Массив навыков пользователя с прогрессом */
  skills: Skill[];

  /** История месячной активности */
  months: Month[];
}
