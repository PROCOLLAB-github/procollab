/** @format */

/**
 * Универсальный интерфейс для пагинированных ответов API
 *
 * Используется для всех эндпоинтов, которые возвращают списки данных
 * с поддержкой постраничной навигации. Следует стандарту Django REST Framework.
 *
 * @template T - Тип элементов в массиве results
 */
export interface ApiPagination<T> {
  /** Общее количество элементов во всей коллекции */
  count: number;

  /** Массив элементов текущей страницы */
  results: T[];

  /** URL для получения следующей страницы (null если это последняя страница) */
  next: string;

  /** URL для получения предыдущей страницы (null если это первая страница) */
  previous: string;
}
